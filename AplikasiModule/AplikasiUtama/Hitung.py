import math

def tambah (bil1, bil2):
    hasil = bil1 + bil2
    print(f'hasil penjumlahan dari {bil1} + {bil2} = {hasil}')

def kurang (bil1, bil2):
    hasil = bil1 - bil2
    print(f'hasil pengurangan dari {bil1} - {bil2} = {hasil}')

def kali (bil1, bil2):
    hasil = bil1 * bil2
    print(f'hasil dari perkalian {bil1} x {bil2} = {hasil}')

def bagi (bil1, bil2):
    hasil = bil1 / bil2
    print(f'hasil dari pembagian {bil1} : {bil2} = {hasil}')

def pangkat (bil1, bil2):
    hasil = math.pow (bil1,bil2)
    print(f'hasil dari penjumlahan {bil1} ^ {bil2} = {hasil}')


